package main.java.com.turngame.exception;

/**
 * Исключение, выбрасываемое при возникновении ошибок в процессе сохранения или загрузки игры.
 * <p>
 * Это исключение используется для инкапсуляции всех ошибок, связанных с:
 * <ul>
 *   <li>Сохранением состояния игры в файл</li>
 *   <li>Загрузкой состояния игры из файла</li>
 *   <li>Ошибками сериализации/десериализации игровых данных</li>
 *   <li>Проблемами доступа к файловой системе (отсутствие прав, повреждение файлов и т.д.)</li>
 * </ul>
 * <p>
 * Класс наследуется от {@link Exception}, что делает его проверяемым исключением.
 * Это означает, что вызывающий код должен явно обрабатывать это исключение или
 * объявлять его в своей сигнатуре с помощью ключевого слова {@code throws}.
 *
 * @author Playstall
 * @version 1.0
 * @see Exception
 * @see java.io.IOException
 * @since 1.0
 */
public class SaveLoadException extends Exception {

    /**
     * Создает новое исключение с указанным сообщением об ошибке.
     * <p>
     * Используется для ошибок, которые могут быть описаны простым сообщением
     * без указания первопричины (вложенного исключения).
     *
     * @param message детальное сообщение об ошибке, объясняющее причину исключения.
     *                Сообщение сохраняется для последующего получения через метод
     *                {@link Throwable#getMessage()}.
     * @throws NullPointerException если {@code message} равен {@code null}
     */
    public SaveLoadException(String message) {
        super(message);
    }

    /**
     * Создает новое исключение с указанным сообщением об ошибке и причиной.
     * <p>
     * Используется для ошибок, которые вызваны другими исключениями (например,
     * {@link java.io.IOException} при работе с файлами). Это позволяет сохранить
     * цепочку исключений для более полной диагностики проблемы.
     *
     * @param message детальное сообщение об ошибке, объясняющее причину исключения
     * @param cause   первоначальная причина исключения, обычно другое исключение
     *                (например, {@link java.io.IOException}). Сохраняется для
     *                последующего получения через метод {@link Throwable#getCause()}.
     *                Значение {@code null} разрешено и означает, что причина
     *                неизвестна или не существует.
     * @throws NullPointerException если {@code message} равен {@code null}
     * @see Throwable#getCause()
     */
    public SaveLoadException(String message, Throwable cause) {
        super(message, cause);
    }
}

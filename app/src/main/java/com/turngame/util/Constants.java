package main.java.com.turngame.util;

/**
 * Класс, содержащий константы и настройки игры.
 * <p>
 * Этот класс служит центральным хранилищем для всех конфигурационных параметров игры.
 * Использование констант вместо "магических чисел" в коде делает игру более
 * гибкой, понятной и легкой для балансировки.
 * <p>
 * Класс содержит следующие группы констант:
 * <ul>
 *   <li>Размеры игровой карты</li>
 *   <li>Начальные значения ресурсов</li>
 *   <li>Стоимости строительства и действий</li>
 *   <li>Параметры производства и роста</li>
 *   <li>Условия победы</li>
 *   <li>Настройки файлов</li>
 * </ul>
 * <p>
 * <strong>Важно:</strong> Изменение значений в этом классе напрямую влияет
 * на баланс и сложность игры. Рекомендуется тестировать игру после любых изменений.
 *
 * @author Playstall
 * @version 1.0
 * @see <a href="https://ru.wikipedia.org/wiki/%D0%91%D0%B0%D0%BB%D0%B0%D0%BD%D1%81%D0%B8%D1%80%D0%BE%D0%B2%D0%BA%D0%B0_%D0%B8%D0%B3%D1%80%D1%8B">Балансировка игры</a>
 * @since 1.0
 */
public class Constants {
    /**
     * Ширина игровой карты в клетках.
     * <p>
     * Определяет количество клеток по горизонтали.
     * Влияет на общий размер игрового поля и продолжительность игры.
     */
    public static final int MAP_WIDTH = 10;

    /**
     * Высота игровой карты в клетках.
     * <p>
     * Определяет количество клеток по вертикали.
     * Вместе с {@link #MAP_WIDTH} определяет общее количество клеток на карте.
     */
    public static final int MAP_HEIGHT = 10;

    /**
     * Начальное количество риса у каждого игрока при старте новой игры.
     */
    public static final int INITIAL_RICE = 50;

    /**
     * Начальное количество воды у каждого игрока при старте новой игры.
     */
    public static final int INITIAL_WATER = 50;

    /**
     * Начальное количество крестьян у каждого игрока при старте новой игры.
     * <p>
     * Крестьяне необходимы для сбора воды и захвата территорий.
     */
    public static final int INITIAL_PEASANTS = 3;

    /**
     * Количество риса, необходимое для постройки дома крестьянина.
     *
     * @see main.java.com.turngame.model.Building.BuildingType#PEASANT_HOUSE
     */
    public static final int HOUSE_RICE_COST = 20;

    /**
     * Количество воды, необходимое для постройки дома крестьянина.
     *
     * @see main.java.com.turngame.model.Building.BuildingType#PEASANT_HOUSE
     */
    public static final int HOUSE_WATER_COST = 10;

    /**
     * Количество крестьян, необходимое для постройки дома крестьянина.
     * <p>
     * Эти крестьяне "расходуются" при строительстве, что требует
     * тщательного планирования развития.
     *
     * @see main.java.com.turngame.model.Building.BuildingType#PEASANT_HOUSE
     */
    public static final int HOUSE_PEASANT_COST = 1;


    /**
     * Количество риса, производимого каждым игроком в конце дня.
     * <p>
     * Это базовый прирост риса, который может быть увеличен
     * за счет полива ({@link main.java.com.turngame.model.GameModel.ActionType#WATER_RICE}).
     */
    public static final int RICE_GROWTH_PER_DAY = 5;

    /**
     * Количество воды, собираемое одним крестьянином за день.
     * <p>
     * Общее количество собираемой воды = количество крестьян × WATER_PER_DAY.
     */
    public static final int WATER_PER_DAY = 10;

    /**
     * Количество новых крестьян, производимых одним домом крестьянина в день.
     * <p>
     * Каждый дом крестьянина производит это количество крестьян
     * в конце каждого игрового дня.
     */
    public static final int PEASANT_PRODUCTION_RATE = 1;

    /**
     * Процент территории, который необходимо контролировать для победы.
     * <p>
     * Значение 0.5 означает 50% территории. Для победы игрок должен контролировать
     * не менее этого процента от общего количества клеток ({@link #MAP_WIDTH} × {@link #MAP_HEIGHT}).
     * <p>
     * Формула: общее_количество_клеток × WIN_PERCENTAGE
     */
    public static final double WIN_PERCENTAGE = 0.5;

    /**
     * Имя файла по умолчанию для сохранения игры.
     * <p>
     * Файл сохраняется в текущей рабочей директории приложения.
     *
     * @see main.java.com.turngame.util.GameSaver
     */
    public static final String SAVE_FILE = "game_save.dat";

    /**
     * Имя файла по умолчанию для журналирования событий игры.
     * <p>
     * В этот файл записываются все игровые события, ошибки и действия игроков.
     *
     * @see main.java.com.turngame.util.GameLogger
     */
    public static final String LOG_FILE = "game_log.log";
}
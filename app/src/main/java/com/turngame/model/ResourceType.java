package main.java.com.turngame.model;

/**
 * Перечисление типов ресурсов, доступных в игре.
 * <p>
 * Ресурсы являются основными экономическими единицами в игре и используются для:
 * <ul>
 *   <li>Строительства зданий</li>
 *   <li>Захвата территорий</li>
 *   <li>Поддержания и развития игровой экономики</li>
 *   <li>Определения победителя по косвенным показателям</li>
 * </ul>
 * <p>
 * Каждый тип ресурса имеет как программное имя (используемое в коде),
 * так и отображаемое имя (для показа пользователю).
 *
 * @author Playstall
 * @version 1.0
 * @see Player
 * @see Building
 * @see Tile
 * @since 1.0
 */
public enum ResourceType {
    /**
     * Рис - основной пищевой ресурс в игре.
     * <p>
     * Используется для:
     * <ul>
     *   <li>Строительства зданий</li>
     *   <li>Поддержания населения</li>
     *   <li>Как показатель благосостояния игрока</li>
     * </ul>
     * Рис производится на контролируемых территориях и может быть увеличен
     * с помощью полива.
     */
    RICE("Рис"),

    /**
     * Вода - жизненно важный ресурс для сельского хозяйства.
     * <p>
     * Используется для:
     * <ul>
     *   <li>Полива риса для увеличения урожая</li>
     *   <li>Строительства зданий</li>
     *   <li>Поддержания жизни крестьян</li>
     * </ul>
     * Вода собирается крестьянами и является ограниченным ресурсом,
     * который необходимо распределять между различными нуждами.
     */
    WATER("Вода"),

    /**
     * Крестьяне - человеческий ресурс, представляющий население игрока.
     * <p>
     * Используются для:
     * <ul>
     *   <li>Сбора воды</li>
     *   <li>Захвата новых территорий</li>
     *   <li>Строительства зданий (как ресурс и как строители)</li>
     *   <li>Производства большего количества крестьян (через дома крестьян)</li>
     * </ul>
     * Крестьяне являются основным активным ресурсом, который может выполнять
     * действия и производить другие ресурсы.
     */
    PEASANTS("Крестьяне");

    /**
     * Человеко-читаемое название ресурса для отображения в пользовательском интерфейсе.
     */
    private final String displayName;

    /**
     * Создает новый тип ресурса с указанным отображаемым именем.
     *
     * @param displayName отображаемое имя ресурса на русском языке
     * @throws NullPointerException если {@code displayName} равен {@code null}
     * @throws IllegalArgumentException если {@code displayName} пустая строка
     */
    ResourceType(String displayName) {
        this.displayName = displayName;
    }

    /**
     * Возвращает человеко-читаемое название ресурса для отображения в интерфейсе.
     * <p>
     * Это имя используется во всех элементах пользовательского интерфейса,
     * где требуется отображение типа ресурса.
     *
     * @return локализованное отображаемое имя ресурса
     */
    public String getDisplayName() {
        return displayName;
    }
}